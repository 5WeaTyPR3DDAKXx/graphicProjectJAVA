    public CorsiDialog(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();

        for (Corso a : gestor.gc.visualizzaCorsi()) {

            Object[] row = {a.getCodiceCorso(), a.getNomeCorso(), a.getDurataAnni()};
            model.addRow(row);
            nRighe++;
        }
        jTable1.setModel(model);

        model.addTableModelListener((TableModelEvent e) -> {
            if (check|| e.getColumn() == TableModelEvent.ALL_COLUMNS) {
                return;
            }

            int row = e.getFirstRow();
            int col = e.getColumn();
            String changed = (String) model.getValueAt(row, col);
            try {
                check = true;

                switch (col) {
                    case 1: // Nome
                        if (changed == null || changed.trim().isEmpty() || changed.matches("\\d+")) {
                            model.setValueAt(gestor.gc.cercaCorso((String) model.getValueAt(row, 0)).getNomeCorso(), row, col);
                        } else {
                            // salva nuovo nome
                            gestor.gd.cambiaNome((String) model.getValueAt(row, 0), changed);
                        }
                        break;
                    case 2:
                        if ("".equals(changed) || !changed.matches("\\d+") || Integer.parseInt(changed) < 1) {
                            model.setValueAt(gestor.gc.cercaCorso((String) model.getValueAt(row, 0)).getDurataAnni() + "", row, col);
                        } else {
                            gestor.gc.cambiaAnni((String) model.getValueAt(row, 0), Integer.parseInt(changed));
                        }
                        break;

                }
            } finally {
                check = false;
            }

        });

    }

