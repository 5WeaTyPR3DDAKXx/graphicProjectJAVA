    private void buttonInserisciActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonInserisciActionPerformed
        boolean error = false;

        String nome = nomeInput.getText();
        String durata = durataInput.getText();
        String code = codeInput.getText();

        if ("".equals(nome)) {
            error = true;
            nomeInput.setBorder(new LineBorder(Color.red, 3));
        } else {
            nomeInput.setBorder(new JTextField().getBorder());
        }

        if ("".equals(durata) || !durata.matches(".*\\d.*")) {
            error = true;
            durataInput.setBorder(new LineBorder(Color.red, 3));
        } else {
            durataInput.setBorder(new JTextField().getBorder());
        }

        if ("".equals(code)) {
            error = true;
            codeInput.setBorder(new LineBorder(Color.red, 3));
        } else {
            codeInput.setBorder(new JTextField().getBorder());
        }

        if (error) {
            System.out.println("Errore nell'inserimento");
        } else {
            if (gestor.gc.creaCorso(code, nome, Integer.parseInt(durata))) {
                Object[] nuovaRiga = {code, nome, durata};
                model.addRow(nuovaRiga);
                jTable1.setRowSelectionInterval(nRighe, nRighe);
                nRighe++;
                if (model.getRowCount() > 1) {
                    delete.setBackground(Color.white);
                }
                codeInput.setBorder(new JTextField().getBorder());
                durataInput.setBorder(new JTextField().getBorder());
                nomeInput.setBorder(new JTextField().getBorder());

            } else {
                codeInput.setBorder(new LineBorder(Color.red, 3));
                durataInput.setBorder(new LineBorder(Color.red, 3));
                nomeInput.setBorder(new LineBorder(Color.red, 3));
            }
        }
        // new Studente(string matricola, string nome, string cognome, corso corsoiscritto)
    }//GEN-LAST:event_buttonInserisciActionPerformed

