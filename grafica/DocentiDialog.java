    private void buttonInserisciActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonInserisciActionPerformed
        boolean error = false;
        String matricola = matricolaInput.getText();
        String nome = nomeInput.getText();
        String cognome = cognomeInput.getText();
        String disciplina = (String) disciplinaBox.getSelectedItem();

        if ("".equals(matricola)) {
            error = true;
            matricolaInput.setBorder(new LineBorder(Color.red, 3));
        } else {
            matricolaInput.setBorder(new JTextField().getBorder());
        }

        if ("".equals(nome)) {
            error = true;
            nomeInput.setBorder(new LineBorder(Color.red, 3));
        } else {
            nomeInput.setBorder(new JTextField().getBorder());
        }

        if ("".equals(cognome)) {
            error = true;
            cognomeInput.setBorder(new LineBorder(Color.red, 3));
        } else {
            cognomeInput.setBorder(new JTextField().getBorder());
        }

        if ("".equals(disciplina)) {
            error = true;
            disciplinaBox.setBorder(new LineBorder(Color.red, 3));
        } else {
            disciplinaBox.setBorder(new JComboBox().getBorder());
        }

        if (error) {
            System.out.println("Errore nell'inserimento");
        } else {
            if (gestor.gdoc.inserisciDocente(matricola, nome, cognome, gestor.gd.cercaDisciplinaNome(disciplina))) {
                Object[] nuovaRiga = {matricola, nome, cognome, disciplina};
                model.addRow(nuovaRiga);
                jTable1.setRowSelectionInterval(nRighe, nRighe);
                nRighe++;

                if (model.getRowCount() > 1) {
                    delete1.setBackground(Color.white);
                }
                matricolaInput.setBorder(new JTextField().getBorder());
                nomeInput.setBorder(new JTextField().getBorder());
                cognomeInput.setBorder(new JTextField().getBorder());
                disciplinaBox.setBorder(new JComboBox().getBorder());

            } else {
                nomeInput.setBorder(new LineBorder(Color.red, 3));
                matricolaInput.setBorder(new LineBorder(Color.red, 3));
                cognomeInput.setBorder(new LineBorder(Color.red, 3));
                disciplinaBox.setBorder(new LineBorder(Color.red, 3));
            }

        }
        // new Studente(string matricola, string nome, string cognome, corso corsoiscritto)
    }//GEN-LAST:event_buttonInserisciActionPerformed

    private void delete1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete1ActionPerformed
        int[] selectedRows = jTable1.getSelectedRows();

        if (selectedRows.length == 0) {
            delete1.setBackground(Color.red);

        }
        if (model.getRowCount() > 1 && model.getRowCount() > selectedRows.length) {
            for (int i = selectedRows.length - 1; i >= 0; i--) {
                int row = selectedRows[i];
                String code = (String) model.getValueAt(row, 0);
                gestor.gdoc.elimina(code);
                model.removeRow(row);
                nRighe--;
            }delete1.setBackground(Color.white);
        } else delete1.setBackground(Color.red);
        
    }//GEN-LAST:event_delete1ActionPerformed

