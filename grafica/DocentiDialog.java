    public DocentiDialog(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();
        for (Docente a : gestor.gdoc.visualizzaDocenti()) {

            Object[] row = {a.getMatricolaDocente(), a.getNome(), a.getCognome(), a.getDisciplinaDocente().getNomeDisciplina()};
            model.addRow(row);
            nRighe++;
        }
        jTable1.setModel(model);
        ArrayList<String> boxModel = new ArrayList<>();
        boxModel.add("");
        for (Disciplina c : gestor.gd.visualizzaDiscipline()) {
            boxModel.add(c.getNomeDisciplina());
        }

        String[] comboBoxModel = boxModel.toArray(new String[0]);
        disciplinaBox.setModel(new DefaultComboBoxModel<>(comboBoxModel));
        boxModel.remove(0);
        String[] boxModelChange = boxModel.toArray(new String[0]);
        JComboBox<String> comboBox = new JComboBox<>(boxModelChange);

        TableColumn colonna = jTable1.getColumnModel().getColumn(3);
        colonna.setCellEditor(new DefaultCellEditor(comboBox));

        model.addTableModelListener((TableModelEvent e) -> {
            if (check || e.getColumn() == TableModelEvent.ALL_COLUMNS) {
                return;
            }

            int row = e.getFirstRow();
            int col = e.getColumn();
            String changed = (String) model.getValueAt(row, col);
            try {
                check = true;

                switch (col) {
                    case 1: // Nome
                        if (changed == null || changed.trim().isEmpty() || changed.matches("\\d+")) {
                            model.setValueAt(gestor.gdoc.cercaDocente((String) model.getValueAt(row, 0)).getNome(), row, col);
                        } else {
                            // salva nuovo nome
                            gestor.gdoc.cambiaNome((String) model.getValueAt(row, 0), changed);
                        }
                        break;
                    case 2: // CFU
                        if (changed == null || changed.trim().isEmpty() || changed.matches("\\d+")) {
                            model.setValueAt(gestor.gdoc.cercaDocente((String) model.getValueAt(row, 0)).getCognome(), row, col);
                        } else {
                            // salva nuovo nome
                            gestor.gdoc.cambiaCognome((String) model.getValueAt(row, 0), changed);
                        }
                        break;
                    case 3:
                        String selected = (String) comboBox.getSelectedItem();
                        System.out.println(selected);
                        gestor.gdoc.cambiaDisciplina((String) model.getValueAt(row, 0), gestor.gd.cercaDisciplinaNome(selected));
                        break;
                }
            } finally {
                check = false;
            }

        });
    }

